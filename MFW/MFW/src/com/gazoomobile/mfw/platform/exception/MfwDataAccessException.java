package com.gazoomobile.mfw.platform.exception;

public class MfwDataAccessException extends RuntimeException {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public MfwDataAccessException() {
		super();
	}

	public MfwDataAccessException(String message, Throwable cause) {
		super(message, cause);
	}

	public MfwDataAccessException(String message) {
		super(message);
	}

	public MfwDataAccessException(Throwable cause) {
		super(cause);
	}
	
	

}
